private void validateAndSave() {
    if (txtNome.getText().trim().isEmpty() ||
        txtEmail.getText().trim().isEmpty() ||
        txtDataNascimento.getText().trim().isEmpty() ||
        txtcpf.getText().trim().isEmpty() ||
        txtCelular.getText().trim().isEmpty() ||
        txtSenhaCadastro.getText().trim().isEmpty()) {

        JOptionPane.showMessageDialog(this, "Por favor, preencha todas as informações", "Campos Incompletos", JOptionPane.ERROR_MESSAGE);
    } else {
        saveData();
    }
}

private void saveData() {
    JOptionPane.showMessageDialog(this, "Cadastro realizado com Sucesso!! Aguarde a liberação do Administrador.");
    this.dispose();
}

